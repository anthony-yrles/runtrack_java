/*
 * This is a simple program to declare three variables in Java
* It declares three integers and assigns the value 0 to all three variables
 */

public class DeclarationVariable {
    public static void main(String args[]) {
        int x, y, z; // Declare three integers
        x = y = z = 0; // Assign the value 0 to all three variables
        System.out.println("x: " + x + ", y: " + y + ", z: " + z);
    }
}
