package jour01;

/*
 * This is a simple program to perform some calculations in Java
 * It declares an integer and performs some calculations on it
 */
public class Calcul {
    public static void main(String[] args) {
        int nombre = 7; // Declare an integer and assign the value 7 to it and print it
        System.out.println("Le nombre est " + nombre + "\n");
        nombre += 3; // Add 3 to the number and print it
        System.out.println("Le nombre est " + nombre + "\n");
        nombre *= 5; // Add 5 to the number and print it
        System.out.println("Le nombre est " + nombre + "\n");
    }
}