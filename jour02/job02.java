package jour02;

/*
 * Write a program that displays the numbers from 100 to 1 on the screen.
 */

public class job02 {
    public static void main(String[] args) {

        // Loop from 1 to 100
        for (int i = 100; i > 0; i--) {
            System.out.println(i); // Output the result of each iteration
        }
    }
}
