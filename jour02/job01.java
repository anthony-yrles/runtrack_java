package jour02;

/*
 * Write a program that displays the numbers from 1 to 100 on the screen.
 */

public class job01 {
    public static void main(String[] args) {

        // Loop from 1 to 100
        for (int i = 0; i < 100; i++) {
            System.out.println(i + 1); // Output the result of each iteration
        }
    }
}
